<template>
    <div class="newslist">
        <div class="container">
            <ul class="media-list">
                <li class="media" v-for="article in articles">
                    <div class="media-left">
                        <a v-bind:href="article.url" target="_blank">
                            <img v-bind:src="article.urlToImage" alt="">
                        </a>

                    </div>
                    <div class="media-body">
                        <h4 class="media-heading"><a v-bind:href="article.url" target="_blank">{{article.title}}</a></h4>
                        <h5>{{article.author}}</h5>
                        <p>{{article.description}}</p>
                    </div>

                </li>
            </ul>
        </div>
    </div>
</template>


<script>
    export default{
        name: 'newslist',
        props: ['source'],
        data(){
            return {
                articles : []
            }
        },
        methods: {
            updateSource: function(source) {
                this.$http.get('https://newsapi.org/v1/articles?source=' + source + '&apikey=1126b7ebda004eb7b9d2bcae1cfcc5ba')
                    .then((response)=>{
                        this.articles = response.data.articles;
                    })
            }
        },
        watch: {
            source: function (val) {
                this.updateSource(val);
            }
        }
    }
</script>

